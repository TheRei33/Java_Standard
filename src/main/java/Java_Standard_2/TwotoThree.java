package Java_Standard_2;

public class TwotoThree {
    public static void main(String[] args) {
        String name = "ja" + "va";
        String str = name + 8.0;
        System.out.println(name);
        System.out.println(str);
        System.out.println(7 + " ");
        System.out.println(" " + 7);
        System.out.println(7 + "");
        System.out.println("" + 7);
        System.out.println("" + "");
        System.out.println(7 + 7 + "");
        System.out.println("" + 7 + 7);

        // 문자열으로 변활때는 (""+)으로 시작

    }
}