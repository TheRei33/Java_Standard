package Java_Standard_4;

public class FourtoTwenty {
    public static void main(String[] args) {
        for( int i = 1 ; i <= 5 ; i++) {
            for( int j = 1 ; j <= 5 ; j++) {
                System.out.printf("[%d,%d]", i, j);
            }
            System.out.println();
        }
    }
}
